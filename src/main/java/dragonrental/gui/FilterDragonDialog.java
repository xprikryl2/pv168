    private void filterButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_filterButtonMouseClicked
        Dragon dragonFilter = new Dragon();
        
        String name = nameField.getText();
        if (name.isEmpty()) {
            name = null;
        }
        
        String dragonElement = elementComboBox.getSelectedItem().toString();
        DragonElement element = null;
        if (!dragonElement.isEmpty()) {
            element = (DragonElement)elementComboBox.getSelectedItem();
        }
        
        Integer speed = null;
        if (!speedCheckBox.isSelected()) {
            speed = (Integer) speedSpinner.getValue();
        }
        
        LocalDate birthDate = null;
        if (jXDatePicker1.getDate() != null) {
            try {
                birthDate = LocalDate.parse(jXDatePicker1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate().toString());
            } catch (DateTimeParseException ex) {
                JOptionPane.showMessageDialog(this,
                        bundle.getString("date_parse_error"),
                        bundle.getString("error"),
                        JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        
        dragonFilter.setName(name);
        dragonFilter.setElement(element);
        dragonFilter.setDateOfBirth(birthDate);
        dragonFilter.setMaximumSpeed(speed);
        
        new FilterDragonWorker(dragonFilter,this).execute();
    }//GEN-LAST:event_filterButtonMouseClicked

