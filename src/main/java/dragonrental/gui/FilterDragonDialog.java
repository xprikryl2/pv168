    private class FilterDragonWorker extends SwingWorker<Void, Void> {
        private Dragon dragon;
        private javax.swing.JDialog parent;
        private List<Dragon> dragons;
        
        public FilterDragonWorker(Dragon dragon, javax.swing.JDialog parent) {
            this.dragon = dragon;
            this.parent = parent;
        }
        
        @Override
        protected Void doInBackground() {
            try {
                this.dragons = manager.findDragons(dragon);
            } 
            catch(ServiceFailureException | ValidationException | IllegalEntityException | IllegalStateException | IllegalArgumentException ex) {
                JOptionPane.showMessageDialog(parent,
                ex.getMessage(),
                bundle.getString("error"),
                JOptionPane.ERROR_MESSAGE);
            }
            return null;
        }
        
        @Override
        protected void done(){
            model.setDragons(this.dragons);
            model.fireTableDataChanged();
            parent.dispose();
        }
        
    }
    
